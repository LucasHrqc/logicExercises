
a = float(input("Digite a medida A: "))
b = float(input("Digite a medida B: "))
c = float(input("Digite a medida C: "))

areaq = a*a
areat = a*b / 2.0
areatp = (a+b) / 2.0 * c

print()
print(f"Área do Quadrado: {areaq:.2f}")
print(f"Área do Triângulo: {areat:.2f}")
print(f"Área do Trapézio: {areatp:.2f}")

